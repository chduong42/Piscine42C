int	ft_atoi(char *str)
{
	int i_sign_nb[3];

	i_sign_nb[1] = 1;
	i_sign_nb[2] = 0;
	while ((*str > 0 && *str < '!') || *str > 126)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			i_sign_nb[1] *= -1;
		++str;
	}
	while (*str >= '0' && *str <= '9')
	{
		i_sign_nb[2] = i_sign_nb[2] * 10 + *str - 48;
		++str;
	}
	return (i_sign_nb[1] * i_sign_nb[2]);
}

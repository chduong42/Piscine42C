int	ft_strcmp(char *s1, char *s2)
{
	int cmp;

	while (*s1 && *s2 && *s1 == *s2)
	{
		++s1;
		++s2;
	}
	cmp = *s1 - *s2;
	if (cmp > 0)
		return (1);
	else if (cmp < 0)
		return (-1);
	else
		return (0);
}

int		ft_iterative_factorial(int nb)
{
	int n;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	else
		n = nb - 1;
	while (n > 0)
		nb *= n--;
	return (nb);
}
